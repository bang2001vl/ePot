package exam.nlb2t.epot.RatingProduct;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentManager;
import androidx.fragment.app.FragmentPagerAdapter;

public class RatingDialogTabAdapter extends FragmentPagerAdapter {
    Fragment[] fragments;
    String[] titles;

    public RatingDialogTabAdapter(@NonNull FragmentManager fm, int behavior, Fragment[] fragments, String[] titles) {
        super(fm, behavior);
        this.fragments = fragments;
        this.titles = titles;
    }

    @NonNull
    @Override
    public Fragment getItem(int position) {
        return fragments[position];
    }

    @Override
    public int getCount() {
        return titles.length;
    }

    @Nullable
    @Override
    public CharSequence getPageTitle(int position) {
        return titles[position];
    }
}
